package org.com;

public class Test {

	public static void main(String[] args) {

		// for each

		int a[] = { 10, 20, 30, 40, 50 };

		for (int b : a) {

			System.out.println(b);

		}

		System.out.println("=======================");

		// for Loop

		for (int i = 0; i < a.length; i++) {

			System.out.println(a[i]);

		}

	}

}
